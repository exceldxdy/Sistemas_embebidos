			LIST P = 16F887
			#INCLUDE<P16F887.INC>

			ORG 0x00
			GOTO PRINCIPAL
			ORG 0x04
			
PRINCIPAL
			BSF STATUS,0x05
			BCF STATUS,0x06
			MOVLW 0x00
			MOVWF TRISB
			MOVWF TRISC
			BCF STATUS,0x05
			BSF STATUS,0x06
			BCF WDTCON,0x00
			BCF STATUS,0x06
			CALL CONTAR
CONTAR
			MOVLW 0xF6
			MOVWF 0x20
DECENAS
			MOVLW 0xF6
			MOVWF 0x21
			MOVF 0x20,0x00
			CALL DISPLAY
			MOVWF PORTC
UNIDADES
			MOVF 0x21,0x00
			CALL DISPLAY
			MOVWF PORTB
			CALL RETARDO
			INCFSZ 0x21
			GOTO UNIDADES
			INCFSZ 0x20
			GOTO DECENAS
			RETURN
RETARDO
			MOVLW 0xEB
			MOVWF 0x22
MONDONGO
			MOVLW 0x3E
			MOVWF 0x23
BUCLE
			MOVLW 0x00
			MOVWF 0x24
CICLO
			NOP
			NOP
			INCFSZ 0x24
			GOTO CICLO
			NOP
			NOP
			NOP
			NOP
			INCFSZ 0x23
			GOTO BUCLE
			INCFSZ 0x22
			GOTO MONDONGO
			RETURN

DISPLAY
			SUBLW 0xFF
			ADDWF PCL
			RETLW 0x3F
			RETLW 0x06
			RETLW 0x5B
			RETLW 0x4F
			RETLW 0x66
			RETLW 0x6D
			RETLW 0x7D
			RETLW 0x07
			RETLW 0x7F
			RETLW 0x67
			END